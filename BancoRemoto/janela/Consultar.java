    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        // TODO add your handling code here:
        LimparJtable();
        consultaPessoaBean = null;
        PessoaDao pessoaDao = new PessoaDao();
        if (jRadioButtonCodigo.isSelected()) {
            consultaPessoaBean = pessoaDao.selectPessoa(jTextNome.getText(), 1);
        }
        if (jRadioButtonNome.isSelected()) {
            consultaPessoaBean = pessoaDao.selectPessoa(jTextNome.getText(), 2);
        }

        if (consultaPessoaBean.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nada foi encontrado !!!");
            jTextNome.requestFocus();
            return;
        }

        MostrarDados();
    }//GEN-LAST:event_jButtonConsultarActionPerformed

