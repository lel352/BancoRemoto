    private void MostrarDados() {
        DefaultTableModel model = (DefaultTableModel) jTableDados.getModel();
        for (int i = 0; i < consultaPessoaBean.size(); i++) {
            model.addRow(new Object[]{"", "", ""});
            ConsultaPessoaBean consultaPessoa = consultaPessoaBean.get(i);
            model.setValueAt(consultaPessoa.getCodiPessoa(), i, 0);//Pessoa
            model.setValueAt(consultaPessoa.getNomePessoa(), i, 1);//Pessoa
            model.setValueAt(consultaPessoa.getNomeLogradouro(), i, 2);//Logradouro
            model.setValueAt(consultaPessoa.getNomeBairro(), i, 3);//Bairro
            model.setValueAt(consultaPessoa.getNomeCidade(), i, 4);//Cidade
            model.setValueAt(consultaPessoa.getNomeEstado(), i, 5);//Estado
            model.setValueAt(consultaPessoa.getNomePais(), i, 6);;//Pais            
        }
        ConsultaPessoaBean consultaPessoa = consultaPessoaBean.get(0);
        codigo = consultaPessoa.getCodiPessoa();
        CadPessoa.jTextCodigo.setText(String.valueOf(codigo));//pessoa
        CadPessoa.jTextNome.setText(consultaPessoa.getNomePessoa());
        CadPessoa.codLogradouro = consultaPessoa.getCodiLogradouro(); //Logrado
        CadPessoa.jTextLogradouro.setText(consultaPessoa.getNomeLogradouro());
        CadPessoa.codBairro = consultaPessoa.getCodiBairro(); //Bairro
        CadPessoa.jTextBairro.setText(consultaPessoa.getNomeBairro());
        CadPessoa.codCidade = consultaPessoa.getCodiCidade(); //Cidade
        CadPessoa.jTextCidade.setText(consultaPessoa.getNomeCidade());
        CadPessoa.codEstado = consultaPessoa.getCodiEstado(); //Estado
        CadPessoa.jTextEstado.setText(consultaPessoa.getNomeEstado());
        CadPessoa.codPais = consultaPessoa.getCodiPais(); //Pais
        CadPessoa.jTextPais.setText(consultaPessoa.getNomePais());
    }

